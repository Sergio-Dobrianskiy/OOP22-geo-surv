package it.unibo.geosurv.control.weapons;

/**
 * Manages all weapon types in the game.
 */
public enum WeaponType {
    /**
     * Automatic gun.
     */
    AUTO_GUN,
    /**
     * Creates explosions.
     */
    EXPLOSION_GUN,
    /**
     * Shots lasers.
     */
    LASER_GUN,
    /**
     * Creates satellites.
     */
    SATELLITE_GUN,
}
